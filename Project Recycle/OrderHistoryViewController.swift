import UIKit

class OrderHistoryViewController: UIViewController {

    @IBOutlet weak var completedOrdersTableView: UITableView!
    
    var completedOrderItems: [Order] = []
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
    }
}

extension OrderHistoryViewController: UITableViewDelegate {
    
}

extension OrderHistoryViewController: UITableViewDataSource {
    func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }
    
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return completedOrderItems.count
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "completedOrderItemCell", for: indexPath)
        
        return cell
    }
}
